public class UsuarioADM {
    String re;
    String senha;

    public UsuarioADM() {
    }

    public UsuarioADM(String re, String senha) {
        this.re = re;
        this.senha = senha;
    }

    public String getRe() {
        return re;
    }

    public void setRe(String re) {
        this.re = re;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    
    
}
