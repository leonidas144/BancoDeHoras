    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        
        addhoras.setRe(txtRE.getText());
        addhoras.setHoras(Float.parseFloat(txtHoras.getText()));
        
        switch(JOptionPane.showConfirmDialog(null,"Confirma a adição de " + addhoras.getHoras() +"\nPara o RE: " + addhoras.getRe(), "Confirmação de alteração",JOptionPane.YES_NO_OPTION)){
            case 0:
                adicionarHoras (addhoras);
                JOptionPane.showMessageDialog(null, "Alteração realizada!!");
            break;
            case 1:
                JOptionPane.showMessageDialog(null, "Lançamento de alteração cancelado!!");
            break;
        
        };
        
        
        
    }//GEN-LAST:event_btnAdicionarActionPerformed

