    public void adicionarHoras (AddHoras addhoras){
        this.conectar.conectaBanco();
        
        addhoras.setRe(txtRE.getText());
        addhoras.setData(txtData.getText());
        addhoras.setHoras(Float.parseFloat(txtHoras.getText()));
        addhoras.setObs(txtObs.getText());
        
        try{
            this.conectar.executarSQL(
                "CALL addhora "
                + "(" + addhoras.getRe() + ","
                + "'" + LocalDate.parse(addhoras.getData(),fmt) + "',"
                + addhoras.getHoras() + ","
                + "'" + addhoras.getObs() + "');"         
                );
        }catch (Exception e){
            System.out.println("Erro ao lançar horas!!" + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao lançar horas!!");
        }finally{
            this.conectar.fechaBanco();
            
        }
    }
    
